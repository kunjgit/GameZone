var audio_url=window.URL || window.webkitURL;

function audio_playsound(params)
{
  try
  {
    var soundURL=jsfxr(params);
    var player=new Audio();

    player.src=soundURL;
    player.play();
    player.addEventListener('ended', function(e) {
      audio_url.revokeObjectURL(soundURL);
      }, false);
  }

  catch (e) {}
}

